#include <iostream>

// Function declaration
void func(void);

static int count = 20; /* Global variable */

main() {

   while(count--) {
      func();
   }

   return 0;
}

// Function definition
void func( void ) {
   static int i = 5; // local static variable
   i++;
   std::cout << "i is " << i ;
   std::cout << " and count is " << count << std::endl;
}
